extension TryOrNullOnFunctionExtension on Function {
  T? tryOrNull<T>(
    List<dynamic>? positionalArguments, [
    Map<Symbol, dynamic>? namedArguments,
  ]) {
    try {
      return Function.apply(
        this,
        positionalArguments ?? [],
        namedArguments ?? {},
      ) as T?;
    } catch (_) {
      return null;
    }
  }
}
