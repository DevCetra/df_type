import 'dart:async' show FutureOr;

// ░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░

FutureOr<R> mapSyncOrAsync<T, R>(
  FutureOr<T> value,
  TSyncOrAsyncMapper<T, R> callback, {
  void Function(Object e)? onError,
}) {
  if (value is Future<T>) {
    return value.then(
      callback,
      onError: onError,
    );
  } else {
    try {
      return callback(value);
    } catch (e) {
      onError?.call(e);
      rethrow;
    }
  }
}

